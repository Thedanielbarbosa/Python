# Faça um código que solicita ao usuário dois números e faça a soma dos dois e mostre os resultados

# solicitando ao usuário os números
n1 = int(input('Digite um numero: '))
n2 = int(input('Digite o segundo numero: '))

# Fazendo a soma dos números
s = n1 + n2

# imprimindo a soma dos números
print('A soma de {} e {} vale {}!'.format(n1, n2, s))
